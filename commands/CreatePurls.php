    public function execute() {

        $commandresult = new ComandoResult();

        $raw_users = ORM::for_table('RawUser2')->find_many();
        $user_count = 0;
        foreach($raw_users as $raw_user) {

            $company = ORM::for_table(COMPANY_TABLE)->where('company_name', $raw_user->partner)->find_one();

            if($company == null) {
                $company = ORM::for_table(COMPANY_TABLE)->create();
                $company->company_name=$raw_user->partner;
                $company->save();
            }

            $user = ORM::for_table(USER_TABLE)->create();
            $user->company_id=$company->id();
            $user->first_name=$raw_user->fornavn;
            $user->last_name=$raw_user->efternavn;
            $user->email=$raw_user->email;

            $fname = $this->remove_accent(utf8_encode($user->first_name));
            $fname = preg_replace('/[^a-z]/i', '', $fname);

            $lname = $this->remove_accent(utf8_encode($user->last_name));
            $lname = preg_replace('/[^a-z]/i', '', $lname);

            $purl = 'http://www.deudvalgte.dk/'.$fname.'/'.$lname;
            $ruser = ORM::for_table('RawUser')->where('purl', $purl)->find_one();
            while($ruser != null) {
                $purl = 'http://www.deudvalgte.dk/'.floor(rand(100,999)).'/'.$fname.'/'.$lname;
                $ruser = ORM::for_table('RawUser')->where('purl', $purl)->find_one();
            }

            $user->purl=$purl;
            $user->save();

            $raw_user->purl=$purl;
            $raw_user->user_id=$user->id();
            $raw_user->save();

            $user_count++;
        }

        $commandresult->setData('result',array('users_created'=>$user_count, 'users_expected' => count($raw_users)));

        return $commandresult;
    }


